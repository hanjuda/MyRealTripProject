package com.join.vo;

public class See_moreVO {
	
	private String see_name;
	private String background_img;
	private String menu;
	private String span1;
	private String span2;
	
	public See_moreVO() {}

	public See_moreVO(String see_name, String background_img, String menu, String span1, String span2) {
		this.see_name = see_name;
		this.background_img = background_img;
		this.menu = menu;
		this.span1 = span1;
		this.span2 = span2;
	}

	public String getSee_name() {
		return see_name;
	}

	public void setSee_name(String see_name) {
		this.see_name = see_name;
	}

	public String getBackground_img() {
		return background_img;
	}

	public void setBackground_img(String background_img) {
		this.background_img = background_img;
	}

	public String getMenu() {
		return menu;
	}

	public void setMenu(String menu) {
		this.menu = menu;
	}

	public String getSpan1() {
		return span1;
	}

	public void setSpan1(String span1) {
		this.span1 = span1;
	}

	public String getSpan2() {
		return span2;
	}

	public void setSpan2(String span2) {
		this.span2 = span2;
	}
	
	
	

	
}
